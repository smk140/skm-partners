// ID ÏÉùÏÑ± Ìï®Ïàò
export function generateId(items: any[]) {
  if (!items || items.length === 0) {
    return 1
  }
  const maxId = Math.max(0, ...items.map((item) => Number(item.id) || 0))
  return maxId + 1
}
